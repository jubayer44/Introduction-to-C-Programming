#include <stdio.h>
#include <stdbool.h>

int main() {
    
    // Variable declaration
    int a = 5;
    float b = 3.2;
    char c = 'A';
    bool e = true;
    
    // Print statements
    printf("Integer: %d\n", a);
    printf("Float: %.2f\n", b);
    printf("Character: %c\n", c);
    printf("Boolean: %s\n", e ? "true" : "false");
    
    return 0;
}